    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String nome;
        String cpf;
        String telefone;
        String crm;
        Date admissao;
        String email;
        boolean resultado;

        if (txtNome.getText().trim().length() < Constants.TAM_MIN_NOME
                || txtNome.getText().trim().length() > Constants.TAM_MAX_NOME) {
            showMessageWarning("Nome inválido");
            txtNome.requestFocus();
        } else {
            nome = txtNome.getText().trim();
            if (txtCpf.getText().equals(Constants.FORMATO_TXT_CPF)) {
                showMessageWarning("CPF inválido!");
                txtCpf.requestFocus();
            } else {
                cpf = txtCpf.getText();
                if (txtTelefone.getText().equals(Constants.FORMATO_TXT_TELEFONE)) {
                    showMessageWarning("Telefone inválido!");
                    txtTelefone.requestFocus();
                } else {
                    telefone = txtTelefone.getText();
                    if (txtCrm.getText().trim().length() < Constants.TAM_MIN_CRM
                            || txtCrm.getText().trim().length() > Constants.TAM_MAX_CRM) {
                        showMessageWarning("CRM inválido");
                        txtCrm.requestFocus();
                    } else {
                        crm = txtCrm.getText();
                        if (txtAdmissao.getValue() == null) {
                            showMessageWarning("Data de admissão inválida!");
                            txtAdmissao.requestFocus();
                        } else {
                            admissao = (Date) txtAdmissao.getValue();

                            if (txtEmail.getText().trim().length() < Constants.TAM_MIN_EMAIL
                                    || txtEmail.getText().trim().length() > Constants.TAM_MAX_EMAIL) {
                                showMessageWarning("E-mail inválido");
                                txtEmail.requestFocus();
                            } else {
                                email = txtEmail.getText().trim();

                                if (idMedico == null) {
                                    resultado = MedicoController.getInstance().createMedico(nome, cpf, telefone, crm, admissao, email);
                                    if (resultado == true) {
                                        showMessageWarning("Falha ao cadastrar o médico!");
                                    } else {
                                        JOptionPane.showMessageDialog(this, "Médico cadastrado com sucesso!");
                                        limparCamposCadastro();
                                    }
                                } else {
                                    resultado = MedicoController.getInstance().updateMedicoById(idMedico, nome, cpf, telefone, crm, admissao, email);
                                    if (resultado == true) {
                                        showMessageWarning("Falha ao atualizar o médico!");
                                    } else {
                                        JOptionPane.showMessageDialog(this, "Médico atualizado com sucesso!");
                                        this.dispose();
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void showMessageWarning(String mensagem) {
        JOptionPane.showMessageDialog(this, mensagem, "Aviso", JOptionPane.WARNING_MESSAGE);
    }

    private void limparCamposCadastro() {
        txtNome.setText("");
        txtCpf.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");
        txtAdmissao.setText("");
        txtCrm.setText("");
    }

