    private void preencherDadosMedico() {
        //Consulta medico no banco de dados pelo id
        Medico medico = MedicoController.getInstance().readMedicoById(idMedico);

        if (medico != null) {
            txtCodigo.setText(String.valueOf(medico.getId()));
            txtNome.setText(medico.getNome());
            txtCpf.setText(medico.getCpf());
            txtTelefone.setText(medico.getTelefone());
            txtCrm.setText(medico.getCrm());
            txtEmail.setText(medico.getEmail());
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            txtAdmissao.setText(dateFormat.format(medico.getDataAdmissao()));

            txtAdmissao.requestFocus();
        }

    }

